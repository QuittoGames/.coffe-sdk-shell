# ======================================

icons::list() {
    echo "=== Coffee SDK Icons ==="
    echo ""

    local vars
    vars=$(compgen -v | grep '^ICON_' | sort)

    for var in $vars; do
        local val="${!var}"
        echo "  ${var}: ${val}"
    done
}
